

//index ++ ==> index=index+1
//index -- ==> index=index-1
var index =10;
var y = index +1;//11
console.log(`index: ${index}, y:${y}`);
var y = index ++;//11
console.log(`index: ${index}, y:${y}`);
console.log(`------------------`);
var y = index -1;
console.log(`index: ${index}, y:${y}`);
var y = index --;
console.log(`index: ${index}, y:${y}`);
